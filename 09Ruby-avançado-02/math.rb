# Math, modulo nativo do ruby para realizar operações matematica

puts Math.sqrt(64) # raiz quadrada
puts Math.log10(1000) # logaritmo na base 10

radian = 30 * (Math::PI / 180) # graus para radianos
puts Math.cos(radian) # cosseno de 30

puts Math::PI # valor de Pi (dois pois é um module)
puts Math::E # valor de e
